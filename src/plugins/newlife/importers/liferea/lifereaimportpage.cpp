	QString LifereaImportPage::GetSuggestion () const
	{
		QDir home = QDir::home ();
		QStringList entries = home.entryList (QStringList (".liferea_*"),
				QDir::Dirs | QDir::Hidden,
				QDir::Name);

		QString defaultFile;
		if (entries.size ())
			defaultFile = QDir::homePath () + "/" + entries.last ();

		return defaultFile;
	}
